// 2에서 5 까지의 숫자를 뽑고 싶다. 제대로 작동하도록 (가) ~ (라)를 모두 채워라.
// 반복문의 시작 값과 끝 값을 입력하는 스타일에 관련된 문제다

// code 1
const answer = [];
for (let n = 0; n < (가); n++) {
  answer.push(n + 2);
}

// code 2
for (let n = 0; n <= (나); n++) {
  answer.push(n + 2);
}

// code 3
for (let n = 1; n < (다); n++) {
  answer.push(n + 1);
}

// code 4
for (let n = 1; n <= (라); n++) {
  answer.push(n + 1);
}

// 답 : 가 = 4 , 나 = 3 , 다 = 5 , 라 = 4